public class saled {
    private int code;
    private int article;

    public saled(int code, int article) {
        this.code = code;
        this.article = article;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getArticle() {
        return article;
    }

    public void setArticle(int article) {
        this.article = article;
    }
}
